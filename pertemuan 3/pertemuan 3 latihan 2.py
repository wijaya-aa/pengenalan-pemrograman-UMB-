#Diketahui variabel dengan nilai 15,4,4.Buatlah coding dengan ketentuan berikut
#Tampilkan ketiga variabel tersebut
x=15
y=4
z=4

print("x =",x)
print("y =",y)
print("z =",z)

#Bandingkan antara variabel satu dan ketiga apakah variabel tersebut memiliki nilai yang sama?
print("x == z hasilnya adalah ",x==z)

#Bandingkan antara nilai variabel kedua dan ketiga , apakah variabel tersebut memiliki nilai yang sama?
print("y == z hasilnya adalah ",y==z)

#Bandingkan antara variabel kedua dan ketiga, apakah variabel tersebut tidak sama?
print("y != z hasilnya adalah ",y!=z)

